int longestIncreasingSubSequence(vector<int> userInput) {
    vector<int> auxSpace;
    auxSpace.push_back(1);
    int maxLength = 0;
    for (unsigned int outerCrawler = 1; outerCrawler < userInput.size() - 1; outerCrawler++) {
        int maxLengthForCurrentValue = 1;
        for (unsigned int innerCrawler = 0; innerCrawler < outerCrawler; innerCrawler++) {
            if (userInput[innerCrawler] < userInput[outerCrawler]) {
                maxLengthForCurrentValue = max(maxLengthForCurrentValue, 1 + auxSpace[innerCrawler]);
            }
        }
        auxSpace.push_back(maxLengthForCurrentValue);
        maxLength = max(maxLength, maxLengthForCurrentValue);
    }
    return maxLength;

}

