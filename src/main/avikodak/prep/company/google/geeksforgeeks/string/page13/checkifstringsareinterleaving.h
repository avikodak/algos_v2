/****************************************************************************************************************************************************/

/****************************************************************************************************************************************************/
/*                                                            O(N) Algorithm                                                                        */
/****************************************************************************************************************************************************/
bool checkIfStringAreInterleaving(string firstInput, string secondInput, string result, int firstIndex, int secondIndex, int resultIndex) {
    if (firstIndex == firstInput.size() && secondIndex == secondInput.size()) {
        return resultIndex == result.size();
    }
    if (firstIndex < firstInput.size() && firstInput[firstIndex] == result[resultIndex]) {
        return checkIfStringAreInterleaving(firstInput, secondInput, result, firstIndex + 1, secondIndex, resultIndex + 1);
    } else if (secondIndex < secondInput.size() && secondInput[secondIndex] == result[result]) {
        return checkIfStringAreInterleaving(firstInput, secondInput, result, firstIndex, secondIndex + 1, resultIndex + 1);
    }
    return false;
}

