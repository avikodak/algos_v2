#ifndef REVERSESTACK_H_
#define REVERSESTACK_H_

void reverseStack(stack<itNode *> &auxSpace) {
    if(auxSpace.empty()) {
        return;
    }
    int value = auxSpace.top();
    auxSpace.pop();
    reverseStack(auxSpace);
    auxSpace.push(value);
}

#endif /* REVERSESTACK_H_ */

