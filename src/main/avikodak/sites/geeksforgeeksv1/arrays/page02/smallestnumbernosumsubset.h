/****************************************************************************************************************************************************/
/*                                                                     O(N) Algorithm                                                                     */
/****************************************************************************************************************************************************/
//Tested
int smallestNumberNoSubset(vector<int> userInput) {
    if(userInput.size() == 0) {
        return 1;
    }
    int result = 1;
    for(unsigned int counter = 0; counter < userInput.size() && userInput[counter] <= result; counter++) {
        result += userInput[counter];
    }
    return result;
}

